def get_lang() -> str:
    try:
        from utils import state_manager as sm
        lang = sm.get_ui_language()
    except Exception:
        lang = st.session_state.get("ui_language", "ar")
    return lang if lang in STRINGS else "ar"


def t(key: str, **kwargs) -> str:
    lang = get_lang()
    text = STRINGS.get(lang, STRINGS["ar"]).get(key, STRINGS["ar"].get(key, key))
    if kwargs:
        try:
            return text.format(**kwargs)
        except (KeyError, ValueError):
            return text
    return text


def is_rtl() -> bool:
    return get_lang() == "ar"
